package caracole;

public abstract class StackMethods extends StackBean {
    public void shuffleStack() {
        // change randomly cards order
    }

    public void halfCutStack() {
        // pick random number of top cards (around half of stack) and put it bottom
    }

    public void stackUp() {
        // add card(s) on top of stack
    }

    public void stackDown() {
        // add card(s) on bottom of stack
    }

    public void invertStack() {
        // reverse cards order
    }
}
